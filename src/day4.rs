use std::ops::Add;

pub fn day4_1() {

    //  Matrix of 140 x 140 bytes (65 = A,77 = M,88 = X,83 = S)
    let input = input();

    let mut x_windows = input.chunks_exact(140);
    let mut active_x_lines = [
        x_windows.next().unwrap(),
        x_windows.next().unwrap(),
        x_windows.next().unwrap(),
        x_windows.next().unwrap()
    ];

    let mut count = 0;
    loop {
        let top0 = active_x_lines[0].windows(4);
        let top1 = active_x_lines[1].windows(4);
        let top2 = active_x_lines[2].windows(4);
        let top3 = active_x_lines[3].windows(4);

        let iter = top0.zip(top1).zip(top2).zip(top3);
        for (((line0, line1), line2), line3) in iter {
            // lines
            if check_xmas_line(line0) {
                count = count.add(1);
            }

            if check_xmas_line(line1) {
                count = count.add(1);
            }

            if check_xmas_line(line2) {
                count = count.add(1);
            }

            if check_xmas_line(line3) {
                count = count.add(1);
            }

            // cols
            for col in 0..4 {
                if check_xmas_line(&[
                    line0[col],
                    line1[col],
                    line2[col],
                    line3[col]
                ]) {
                    count = count.add(1);
                }
            }

            // diagonal
            if check_xmas_line(&[
                line0[0],
                line1[1],
                line2[2],
                line3[3]
            ]) {
                count = count.add(1);
            }

            if check_xmas_line(&[
                line0[3],
                line1[2],
                line2[1],
                line3[0]
            ]) {
                count = count.add(1);
            }

        }

        active_x_lines[0] = active_x_lines[1];
        active_x_lines[1] = active_x_lines[2];
        active_x_lines[2] = active_x_lines[3];
        let next_line = x_windows.next();
        if next_line.is_none() {
            break;
        }

        active_x_lines[3] = next_line.unwrap();
    }

    println!("XMAS: {}", count);
}

fn check_xmas_line(line: &[u8]) -> bool {
    (line[0] == 88 && line[1] == 77 && line[2] == 65 && line[3] == 83) || (line[0] == 83 && line[1] == 65 && line[2] == 77 && line[3] == 88)
}

